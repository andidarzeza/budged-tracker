package com.adprod.inventar.models.utils;

public class Time {
    public final static Integer MILLISECOND = 1;
    public final static Integer SECOND = 1000 * MILLISECOND;
    public final static Integer MINUTE = 60 * SECOND;
    public final static Integer HOUR = 60 * MINUTE;
    public final static Integer DAY = 24 * HOUR;
}